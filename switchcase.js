let fruta = "banana";

switch (fruta) {
  case "banana":
    console.log("Banana é rica em potassio");
    break;
  case "maca":
    console.log("Maca é uma otima fonte de fibras");
    break;
  case "laranja":
    console.log("Laranja tem muita vitamina C");
    break;
  default:
    console.log("Fruta desconhecida");
}
